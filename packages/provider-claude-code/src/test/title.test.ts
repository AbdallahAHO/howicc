import { describe, expect, it } from 'vitest'
import type { ParsedRawEntry } from '../jsonl'
import {
  extractSelectedPromptTitle,
  extractUserPromptPreview,
  pickPromptTitleCandidate,
} from '../parse/title'

const entry = (index: number, raw: Record<string, unknown>): ParsedRawEntry => ({
  index,
  raw,
})

describe('title parsing', () => {
  it('skips image-only placeholders when picking a prompt title', () => {
    expect(
      pickPromptTitleCandidate([
        '[Image #13]',
        '[2 images attached]',
        'Set up SSH access for the Hetzner box',
      ]),
    ).toBe('Set up SSH access for the Hetzner box')
  })

  it('extracts the first substantive text block from a user message', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        message: {
          content: [
            {
              type: 'text',
              text: '[Image #4]\n\nDebug why the local OAuth callback is rejected',
            },
          ],
        },
      }),
    ).toBe('Debug why the local OAuth callback is rejected')
  })

  it('normalizes slash-command breadcrumbs into readable prompt previews', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        message: {
          content:
            '<command-message>commit</command-message>\n<command-name>/commit</command-name>\n<command-args>and push</command-args>',
        },
      }),
    ).toBe('/commit and push')
  })

  it('skips local-command caveat prompt previews entirely', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        isMeta: true,
        message: {
          content:
            '<local-command-caveat>Caveat: The messages below were generated by the user while running local commands.</local-command-caveat>',
        },
      }),
    ).toBeUndefined()
  })

  it('formats bash-input breadcrumbs as shell commands', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        message: {
          content: '<bash-input>pnpm test --filter @howicc/provider-claude-code</bash-input>',
        },
      }),
    ).toBe('! pnpm test --filter @howicc/provider-claude-code')
  })

  it('skips local-command stdout when extracting prompt previews', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        message: {
          content:
            '<local-command-stdout>Set model to \u001b[1mOpus 4.6 (1M context) (default)\u001b[22m</local-command-stdout>',
        },
      }),
    ).toBeUndefined()
  })

  it('skips task notifications when extracting prompt previews', () => {
    expect(
      extractUserPromptPreview({
        type: 'user',
        message: {
          content:
            '<task-notification><status>failed</status><summary>Background command "Verify MCP GET still works" failed with exit code 18</summary></task-notification>',
        },
      }),
    ).toBeUndefined()
  })

  it('treats short slash commands as substantive titles before machine output', () => {
    expect(
      pickPromptTitleCandidate([
        '/model',
        'Set model to Opus 4.6 (1M context) (default)',
      ]),
    ).toBe('/model')
  })

  it('prefers the first substantive prompt from the selected thread', () => {
    const selectedEntries = [
      entry(0, {
        type: 'user',
        message: {
          content: [
            {
              type: 'text',
              text: '[Image #9]',
            },
          ],
        },
      }),
      entry(1, {
        type: 'user',
        message: {
          content: [
            {
              type: 'text',
              text: 'Fix the local Docker sync flow and validate the worker auth exchange',
            },
          ],
        },
      }),
    ]

    expect(extractSelectedPromptTitle(selectedEntries)).toBe(
      'Fix the local Docker sync flow and validate the worker auth exchange',
    )
  })
})
